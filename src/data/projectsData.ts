export type { ProjectData } from '../types';

export const getTranslatedProject = (
  slug: string,
  t: (key: string) => string
): ProjectData | undefined => {
  const base = projectsData.find(p => p.slug === slug);
  if (!base) return undefined;

  return {
    ...base,
    subtitle: t(`projects.${slug}.subtitle`),
    description: t(`projects.${slug}.description`),
    contractType: t(`projects.${slug}.contractType`),
    keyMetric: base.keyMetric
      ? { ...base.keyMetric, label: t(`projects.${slug}.keyMetricLabel`) }
      : undefined
  };
};
